//arithmetic operators are used to perform arithmetic operations, operations according to precedence
public class ArithmeticOperators {
    public static void main(String args[]){
        int firstInt = 269;
        int secondInt = 133;
        System.out.println(firstInt*secondInt);//multiplication
        System.out.println(firstInt/secondInt);//division
        System.out.println(firstInt%secondInt);//modulus
        System.out.println(firstInt+secondInt);//addition
        System.out.println(firstInt-secondInt);//subtraction
        System.out.println("end of arithmetic operators");

    }
}
